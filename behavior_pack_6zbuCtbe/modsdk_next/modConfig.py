class ModConfig:

    def __init__(self, modName, modVersion, serverSystemName, serverSystemClsPath, clientSystemName, clientSystemClsPath):
        self.modName = modName
        self.modVersion = modVersion
        self.modServerSystemName = serverSystemName
        self.modServerSystemClsPath = serverSystemClsPath
        self.modClientSystemName = clientSystemName
        self.modClientSystemClsPath = clientSystemClsPath
